import React from "react";
import PropTypes from "prop-types";

const Input = ({ input, label, type, meta: { touched, error } }) => (
  <div>
    <label>{label}</label>
    <div>
      <input {...input} placeholder={label} type={type} />
      {touched && error && <span>{error}</span>}
    </div>
  </div>
);

Input.propTypes = {};

export default Input;
